// ── Deep merge utility ─────────────────────────────────────────────────

function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    if (
      source[key] &&
      typeof source[key] === "object" &&
      !Array.isArray(source[key])
    ) {
      if (!target[key] || typeof target[key] !== "object") {
        target[key] = {};
      }
      deepMerge(target[key], source[key]);
    } else {
      // Only add if not already present (don't overwrite existing keys)
      if (!(key in target)) {
        target[key] = source[key];
      }
    }
  }
  return target;
}

// ── Process each locale ────────────────────────────────────────────────

for (const locale of Object.keys(newKeys)) {
  const filePath = resolve(messagesDir, `${locale}.json`);
  const existing = JSON.parse(readFileSync(filePath, "utf-8"));
  const merged = deepMerge(existing, newKeys[locale]);
  writeFileSync(filePath, JSON.stringify(merged, null, 2) + "\n", "utf-8");
  console.log(`✅ ${locale}.json updated`);
}

